package exercicios_repetição;


public class exercicio_repetição_01 {

		public static void main(String[] args) {
		
		for ( int n = 1000; n >= 1000 && n <= 1999; n++) {
			 if (n % 11 == 5) {
			 System.out.println("O numero " + n + " dividido por 11 resulta em 5");
			 }
			 
		    }
			
	}

}


